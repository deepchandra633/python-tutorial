ch=print("Enter Any Character(a-z)");
match ch:
    case 'a' | 'A':
        print(ch," is a vowel")
    case 'e' | 'E':
        print(ch," is a vowel")
    case 'i' | 'I':
        print(ch," is a vowel")
    case 'o' | 'O':
        print(ch," is a vowel")
    case 'u' | 'U':
        print(ch," is a vowel")
    case _:
        print(ch," not a vowel")